void Toggle_LED(int num)
{
    switch(num)
    {
        case 1:
        {
            if(LED_RED == 1)
            {
                LED_RED = 0;
            }
            else
            {
                LED_RED = 1;
            }
            break;
        }
        case 2:
        {
            if(LED_GREEN == 1)
            {
                LED_GREEN = 0;
            }
            else
            {
                LED_GREEN = 1;
            }
            break;
        }
        case 3:
        {
            if(LED_YELLOW == 1)
            {
                LED_YELLOW = 0;
            }
            else
            {
                LED_YELLOW = 1;
            }
            break;
        }
    }
}
